			0x9e, 0x44, 0xd8, 0x27, 0x23, 0x49, 0x49, 0xbc,
			0x1b, 0x12, 0x34, 0x8e, 0xbc, 0x19, 0x5e, 0xc7
		]
		
		let result = try? AesSiv.aesCtr(aesKey: aesKey, iv: iv, plaintext: plaintext)

		XCTAssertEqual(expected, result)
	}

